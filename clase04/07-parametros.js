// Pasaje de parametros
function sumar(a, b) {
    return a + b
}

sumar(10, 20)            //>  30
sumar(100, 200, 300)       //>  300

console.log(sumar(10, 20))            //>  30
console.log(sumar(100, 200, 300))       //>  300
console.log(sumar(100))       //>  NaN
console.log(sumar())       //> Na